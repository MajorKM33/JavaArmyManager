public class GlobalSettings {
                                                 // GlobalSettings - klasa na często używane zmienne
    private int gold = 1000;
    private int fighterCost = 100;
    private int rangerCost = 200;
    private int mageCost = 300;


    public int getGold() {
        return gold;
    }

    public int getFighterCost() {
        return fighterCost;
    }

    public int getRangerCost() {
        return rangerCost;
    }

    public int getMageCost() {
        return mageCost;
    }
}
